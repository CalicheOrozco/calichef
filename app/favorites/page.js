'use client';
import { useState, useEffect, useContext, useMemo, useRef, useCallback } from 'react';
import { IoClose } from 'react-icons/io5';
import { useAuth } from '@/context/AuthContext';
import { useRouter } from 'next/navigation';
import Navbar from '@/components/Navbar';
import Card from '@/components/Card';
import { CalichefContext } from '@/context/MyContext';

// Componente de carga reutilizable
const LoadingSpinner = () => (
  <>
    <Navbar />
    <div className="container mx-auto py-2 px-4 min-h-screen flex justify-center items-center">
      <div className="text-center">
        <svg
          className="animate-spin h-8 w-8 text-gray-600 mx-auto"
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
        >
          <circle
            className="opacity-25"
            cx="12"
            cy="12"
            r="10"
            stroke="currentColor"
            strokeWidth="4"
          ></circle>
          <path
            className="opacity-75"
            fill="currentColor"
            d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
          ></path>
        </svg>
        <p className="mt-4 text-lg text-gray-600">Loading...</p>
      </div>
    </div>
  </>
);

// Componente de búsqueda
const SearchBar = ({ searchValue, onSearchChange, onSearchClear }) => (
  <form onSubmit={(e) => e.preventDefault()} className="relative mb-8">
    <div className="relative">
      <input
        id="Search"
        className="block rounded-md px-6 pt-6 pb-1 w-full text-md text-white bg-neutral-700 appearance-none focus:outline-none focus:ring-0 peer"
        placeholder=" "
        value={searchValue}
        onChange={onSearchChange}
      />
      <label
        htmlFor="Search"
        className="absolute text-md text-zinc-400 duration-150 transform -translate-y-3 scale-75 top-4 z-10 origin-[0] left-5 peer-placeholder-shown:scale-100 peer-placeholder-shown:translate-y-0 peer-focus:scale-75 peer-focus:-translate-y-3"
      >
        Search
      </label>
      {searchValue && (
        <button
          onClick={onSearchClear}
          className="absolute right-3 top-1/2 transform -translate-y-1/2 text-gray-400 hover:text-white"
          aria-label="Clear search"
          type="button"
        >
          <IoClose className="text-xl" />
        </button>
      )}
    </div>
  </form>
);

// Componente principal
export default function Favorites() {
  const { user, loading } = useAuth();
  const router = useRouter();
  const [favoriteRecipes, setFavoriteRecipes] = useState([]);
  const [loadingRecipes, setLoadingRecipes] = useState(true);
  const [searchRecipes, setSearchRecipes] = useState('');
  const [lastViewedRecipe, setLastViewedRecipe] = useState(null);
  const [highlightedRecipe, setHighlightedRecipe] = useState(null);
  const containerRef = useRef(null);
  
  const { originalData } = useContext(CalichefContext);

  // Cargar favoritos cuando el usuario esté disponible
  useEffect(() => {
    if (loading) return;
    
    if (!user) {
      router.push('/login');
      return;
    }

    if (user.favorites && originalData) {
      const favorites = originalData.filter(recipe =>
        user.favorites.includes(recipe.id)
      );
      setFavoriteRecipes(favorites);
    }
    
    setLoadingRecipes(false);
  }, [user, loading, router, originalData]);
  
  // Efecto para restaurar la posición de scroll y destacar la receta vista anteriormente
  useEffect(() => {
    if (loadingRecipes || !containerRef.current) return;
    
    // Recuperar el índice de la última receta vista
    const lastRecipeIndex = localStorage.getItem('lastRecipeIndex');
    if (lastRecipeIndex !== null) {
      const index = parseInt(lastRecipeIndex, 10);
      if (!isNaN(index) && index >= 0 && index < favoriteRecipes.length) {
        setLastViewedRecipe(favoriteRecipes[index]);
        setHighlightedRecipe(favoriteRecipes[index].id);
        
        // Restaurar la posición de scroll
        const scrollPosition = localStorage.getItem('favoritesScrollPosition');
        if (scrollPosition) {
          window.scrollTo(0, parseInt(scrollPosition, 10));
        }
        
        // Eliminar los datos guardados después de usarlos
        localStorage.removeItem('lastRecipeIndex');
        localStorage.removeItem('favoritesScrollPosition');
        
        // Quitar el destacado después de un tiempo
        setTimeout(() => {
          setHighlightedRecipe(null);
        }, 2000);
      }
    }
  }, [loadingRecipes, favoriteRecipes]);

  // Filtrar recetas usando useMemo para mejorar rendimiento
  const filteredFavorites = useMemo(() => {
    if (!searchRecipes.trim()) return favoriteRecipes;
    
    const searchTerms = searchRecipes.toLowerCase().split(' ').filter(Boolean);
    return favoriteRecipes.filter(recipe =>
      searchTerms.every(term => recipe.title.toLowerCase().includes(term))
    );
  }, [favoriteRecipes, searchRecipes]);

  const handleSearchChange = (event) => {
    setSearchRecipes(event.target.value);
  };

  const clearSearch = () => {
    setSearchRecipes('');
  };
  
  // Función para guardar la posición de scroll y el índice de la receta antes de navegar
  const handleCardClick = useCallback((recipe, index) => {
    // Guardar la posición actual de scroll
    localStorage.setItem('favoritesScrollPosition', window.scrollY.toString());
    // Guardar el índice de la receta seleccionada
    localStorage.setItem('lastRecipeIndex', index.toString());
  }, []);

  if (loading || loadingRecipes) {
    return <LoadingSpinner />;
  }

  return (
    <>
      <Navbar />
      <div className="container mx-auto py-2 px-4 min-h-screen">
        <h1 className="text-2xl font-bold text-white mb-6 pt-4">My Favorite Recipes</h1>

        {favoriteRecipes.length > 0 && (
          <SearchBar 
            searchValue={searchRecipes} 
            onSearchChange={handleSearchChange} 
            onSearchClear={clearSearch} 
          />
        )}

        {filteredFavorites.length > 0 ? (
          <>
            <p className="text-white flex justify-end items-center py-2">
              {filteredFavorites.length} collections found
            </p>
            <div ref={containerRef} className="flex flex-wrap justify-center md:justify-between items-center gap-y-5">
              {filteredFavorites.map((recipe, index) => (
                <div 
                  key={recipe.id} 
                  onClick={() => handleCardClick(recipe, index)}
                  className={`transition-all duration-500 ${highlightedRecipe === recipe.id ? 'ring-4 ring-green-500 scale-105 z-10' : ''}`}
                >
                  <Card
                    id={recipe.id}
                    title={recipe.title}
                    rating_score={recipe.rating_score}
                    rating_count={recipe.rating_count}
                    time={recipe.total_time}
                    img_url={recipe.image_url}
                    category={recipe.category}
                  />
                </div>
              ))}
            </div>
          </>
        ) : (
          <div className="text-center py-10">
            <p className="text-white text-lg mb-4">
              {searchRecipes 
                ? 'No se encontraron recetas con ese término de búsqueda.' 
                : 'No tienes recetas favoritas guardadas.'}
            </p>
            <button
              onClick={() => router.push('/')}
              className="bg-green-500 text-white py-2 px-4 rounded-md hover:bg-green-600 focus:outline-none focus:ring-2 focus:ring-green-500 focus:ring-opacity-50 transition duration-200"
            >
              Explorar Recetas
            </button>
          </div>
        )}
      </div>
    </>
  );
}